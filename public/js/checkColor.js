function checkDirt(dirt) {
    if (dirt > 50) return "#a87383";
    if (dirt > 40) return "#a97abc";
    if (dirt > 30) return "#fe6a69";
    if (dirt > 20) return "#fe9b57";
    if (dirt > 10) return "#fdd74b";
    return "#a8e05f";
}
